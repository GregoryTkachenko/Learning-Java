package com.company.JavaOperators;

//Although the + operator is often used to add together two values, like in the example above,
//it can also be used to add together a variables and value, or a variable and another variable:
public class JavaOperator2 {
    public static void main(String[] arg) {
        int sum1 = 100 + 50;
        int sum2 = 250;
        int sum3 = sum1 + 50;
        int sum4 = sum1 + sum2;
        System.out.println(sum1);
        System.out.println(sum2);
        System.out.println(sum3);
        System.out.print(sum4);
    }
}

/*
Java divides the operators into the following groups:
1.Arithmetic operators
2.Assignment operators
3.Comparison operators
4.Logical operators
5.Bitwise operators
 */