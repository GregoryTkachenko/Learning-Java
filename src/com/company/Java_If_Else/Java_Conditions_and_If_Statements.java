/*
Java supports the usual logical conditions from mathematics:
1. Less than: a > b
2. Greater than: a >b
3. Greater than or equal to: a >= b
Equal to: a == b
Not Equal to: a != b

You can use these conditions to perform different actions for different decisions.

Java has the following conditional statements:
1. Use _ if _ to specify a block of code to be executed, if a specified condition is true
2. Use _ else _ to specify a block of code to be executed, if the same condition is false
3. Use _ else if _ to specify a new condition to test, if the first condition is false
4. Use _ switch - to specify many alternative blocks of code to be executed

*/