package com.company.Java_If_Else;
/*
In the example below we use two variables, x and y,
to test whether x is greater than y (using > operator).
AS x is 20, and y is 18, and we know that 20 is greater than 18,
we print to the screen that "x is greater than y".

 */
public class if_test_Variables {
    public static void main(String[] args) {
        int x = 20;
        int y = 18;
        if (x > y) {
            System.out.println("x is greater than y");
        }
    }
}
