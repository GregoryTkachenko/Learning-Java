package com.company.Java_If_Else;
/*
Use the if statement to specify a block of Java code to be executed if a condition is true

Syntax
  if (condition) {
    // block of code to to be executed if the condition is true
}

Note that if is in lowercase letters. Uppercase letters (IF or IF) will generate an error.

In the example bellow, we test two values to find out if 20 is greater than 18,
If the conditon is true, print some text:

 */
public class The_if_Statement {
    public static void main(String[] args) {
        if (20 > 18) {
            System.out.println("20 is greater than 18");        //obviously
        }
    }
}